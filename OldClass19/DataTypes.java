package OldClass19;

public class DataTypes {
	public static void main (String [] args) {
		String firstName = "Taaraz";
		String lastName = "Sena";
		
		System.out.println(firstName + " " + lastName);
				
		//Primitive 8 types of data type
		//Byte - 8 bit
		byte number =-128;
		byte number2 = 127;
		
		//short - 16 bit
		short x = -32_768;
		short y = 32_767;
		
		//int - 32 bit
		int a = -2_147_483_648;
		int b = 2_147_483_647;
		
		//long - 64 bit - l
		long z = 4252534543654646634l;
		
		//double - decimal 64 bit - d
		double f = 1.33d;
			
		//float - 32 bit  - f
		float g = 1.222f;
		
		//char - character - unicode - 16bit
		char h = '\u00e5';
		System.out.println(h);
		//unicode.com
		
		//boolean - true or false
		boolean s = true;
	}

}
